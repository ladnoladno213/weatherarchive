/**
 * Преобразовать WMO weathercode в W-код (0-9) для W1/W2
 */
function wwToWCode(ww) {
  if (ww == null) return null;
  if (ww === 0) return 0;
  if (ww <= 3) return 1;
  if (ww <= 9) return 4;
  if (ww <= 12) return 4; // туман/дымка
  if (ww <= 19) return 2;
  if (ww <= 29) return 2;
  if (ww <= 39) return 3; // пыльная буря/метель
  if (ww <= 49) return 4; // туман
  if (ww <= 59) return 5; // морось
  if (ww <= 69) return 6; // дождь
  if (ww <= 79) return 7; // снег
  if (ww <= 84) return 8; // ливень
  if (ww <= 90) return 8; // ливень с градом
  if (ww <= 94) return 9; // гроза в прошлом
  if (ww <= 99) return 9; // гроза
  return null;
}

